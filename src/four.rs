
pub fn arithmetic_operations(){

    let num1 = 32;
    let num2 = 64;

    let sum = num1 + num2;
    let sub = num2 - num1;
    let mult = num1 * num2;
    let div = num2 / num1;
    let rem = num2 % 5;

    println!("summation answer is : {}",sum);
    println!("subtraction answer is : {}",sub);
    println!("multiplication answer is : {}",mult);
    println!("division answer is : {}",div);
    println!("remainder answer is : {}",rem);

}